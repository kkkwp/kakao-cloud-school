-- ANSI JOIN

-- CROSS JOIN
SELECT *
FROM EMP, DEPT;

SELECT *
FROM EMP CROSS JOIN DEPT;

-- INNER JOIN
SELECT *
FROM EMP, DEPT
WHERE EMP.DEPTNO = DEPT.DEPTNO

SELECT *
FROM EMP INNER JOIN DEPT
ON EMP.DEPTNO = DEPT.DEPTNO

-- Using - 양쪽 테이블의 동일한 컬럼은 한번만 조회됨
SELECT *
FROM EMP INNER JOIN DEPT
USING (DEPTNO);

-- Natural Join
SELECT *
FROM EMP NATURAL JOIN DEPT;